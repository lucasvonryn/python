"""
Tipo tupla (tuple) - Uma lista imutável
"""
nomes = 'Lucas', 'Maria', 'José' # Uma lista sem os colchetes
idades = ('18', '3', '55') # Também pode ser criada uma tupla com os parênteses
print(nomes)
print(idades)

# Convertendo lista em tuplas
lista = ['oi', 'tchau']
tupla = tuple(lista)
print(lista)
print(tupla)